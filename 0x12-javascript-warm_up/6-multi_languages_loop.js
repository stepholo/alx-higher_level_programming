#!/usr/bin/node
for (let i = 0; i < 3; i++) {
  switch (i) {
    case 0:
      console.log('C is fun');
      break;
    case 1:
      console.log('Python is cool');
      break;
    case 2:
      console.log('JavaScript is amazing');
      break;
    default:
      console.log('Unknown language');
  }
}
